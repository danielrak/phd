# Sur c -------------------------------------------------------------------

for (i in agedepvars_c %>% 
     (function (a) a[str_detect(a, "_norm")])) {
  assign(paste("ageivrel0_excl_", 
               i, 
               "_tous_c",
               sep = ""),
         ivreg(as.formula(paste(
           i, " ~ ", 
           c("age_abs", 
             "I(age_abs - p_age_abs)",
             paste("mois_", 2:11, sep = ""), 
             paste("p_mois_", 2:11, sep = ""),
             "sexe", "pcsR", "cohorte") %>% 
             paste(collapse = " + "),
           "|. - age_abs + mois_1",
           "- I(age_abs - p_age_abs) + I(mois_1 - p_mois_1)",
           sep = ""
         )),
         data = c))
}

# InfÃ©rence et narsq ------------------------------------------------------

for (i in ls()[str_detect(ls(), "^ageiv")]) {
  assign(paste("ct.", i, sep = ""),
         robust.se(get(i)))
  assign(paste("n.", i, sep = ""), nobs(get(i)))
  assign(paste("arsq.", i, sep = ""), ext_adjrsq(get(i)))
  assign(paste("resid.", i, sep = ""), resid(get(i)))
}

